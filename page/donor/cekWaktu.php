<script>
 // Fungsi untuk menampilkan elemen dengan style="display: none;"
  function tampilkanElemen() {
    var elements = document.querySelectorAll('[style="display: none;"]');
    elements.forEach(function(element) {
      element.style.display = 'block';
    });

    // Mengisi data nama dan menampilkan pesan "Sudah Bisa" jika diasumsikan sudah bisa donor
    var namaSpan = document.getElementById('namaSpan');
    var alertBisa = document.querySelector('.alert-bisa');
    var alertTidak = document.querySelector('.alert-tidak')

    var nama = 'Asep';  
    var bisaDonor = true;  

    namaSpan.textContent = nama;

    if (bisaDonor) {
      alertBisa.style.display = 'block';
    } else {
      alertTidak.style.display = 'block';
    }

    // Mengisi data ke elemen-elemen formulir
    var idDonor = document.getElementById('idDonor');
    var namaUser = document.getElementById('namaUser');
    var golDarah = document.getElementById('golDarah');
    var kembaliDonor = document.getElementById('kembaliDonor');

    idDonor.value = '1234';
    namaUser.value = nama;
    golDarah.value = 'B';
    
    var tglKembaliDonor = new Date();
    tglKembaliDonor.setDate(tglKembaliDonor.getDate() + 1);
    var tahun = tglKembaliDonor.getFullYear();
    var bulan = String(tglKembaliDonor.getMonth() + 1).padStart(2, '0');
    var tanggal = String(tglKembaliDonor.getDate()).padStart(2, '0');
    kembaliDonor.value = tahun + '/' + bulan + '/' + tanggal;

    var terakhirDonor = document.getElementById('terakhirDonor');
    var tkp = document.getElementById('tkp');
    var PM = document.getElementById('PM');
    var PHB = document.getElementById('PHB');
    var pengambilanDarah = document.getElementById('pengambilanDarah');

    var tglDonorTerakhir = new Date();
    tglDonorTerakhir.setMonth(tglDonorTerakhir.getMonth() - 1);
    var tahunTerakhir = tglDonorTerakhir.getFullYear();
    var bulanTerakhir = String(tglDonorTerakhir.getMonth()).padStart(2, '0');
    var tanggalTerakhir = String(tglDonorTerakhir.getDate()).padStart(2, '0');
    terakhirDonor.value = tahunTerakhir + '/' + bulanTerakhir + '/' + tanggalTerakhir;

    tkp.value = 'PMI KOTA BANDUNG';
    PM.value = 'Lolos';
    PHB.value = 'Disetujui';
    pengambilanDarah.value = '-';
  }
  

  var btnCari = document.getElementById('btnCari');
  btnCari.addEventListener('click', tampilkanElemen);</script>


